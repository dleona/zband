					<span class="article-left-heading"><span style="font-size: 85px;">H</span>ow does it work? </span>
					With the ZBand you set the alarm on your Android or Windows device, connect through bluetooth and the band does the rest. It will vibrate when the time is right causing no
					disturbance to those around you. By simply pressing the Z button once the ZBand will snooze for a time set by you. Holding down the Z button for
					3 seconds turns off the alarm. Once the alarms are set on the ZBand it does not need to be connected to the Android or Windows device. On a 30 minute charge, the ZBand will last for 10 days with regular use.
